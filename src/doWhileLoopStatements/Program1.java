//WAP TO PRINT HII 5 TIMES USING DO WHILE STATEMENT

package doWhileLoopStatements;

public class Program1 
{
	public static void main(String[] args)
	
	{
		int i=1;
		
		do
		{
			System.out.println("HII");
			i++;
		}
		while(i<=5);
			
//       *************************************************
		
//   WAP TO PRINT 1 TO 5 NUMBER
		
		int j=1;
		
		do
		{
			System.out.println(j);
			j++;
		}
		while(j<=5);
			
//	WAP TO PRINT NUMBER 5 TO 1
		
		int k=5;
		
		do
		{
			System.out.println(k);
			k--;
		}
		while(k>=1);
			
			
			
			
			
			
			
			
			
			
			
			
			
	}

}
