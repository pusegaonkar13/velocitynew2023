// WAP TO PRINT P IN FIVE IN TO FIVE MATRIX

package nestedForLoopStatement;

public class FiveIntofiveMatrix 
{
	public static void main(String[] args) 
	
	{
		for(int i=1;i<=5;i++)
		{
			
			for(int j=1;j<=5;j++)
			{
				System.out.print("P ");
			}
			System.out.println( );
		}
		
		
//		Print cell 5*5 matrix
		
		for(int k=1;k<=5;k++)
		{
			for(int j=1;j<=5;j++)
			{
				System.out.print("Cell ");
			}
			System.out.println("Cell ");
		}
		
		
		
		
	}

}
