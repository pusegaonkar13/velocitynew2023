package homePractic;

public class NestedForLoop 

{
	public static void main(String[] args)
	
	{
		for(int i =1;i<=5;i++)
		{
			for(int j=1;j<=5;j++)
			{
				System.out.print("*");
			}
			System.out.println();
		}
		
		// WAP TO PRINT STAR PATTREN WITH SPACE
		
//	    * * * * *
//	    * * * * *
//	    * * * * *
//	    * * * * *
//	    * * * * *

		for(int i =1;i<=5;i++)
		{
			for(int j=1;j<=5;j++)
			
			{
			System.out.print("* ");
			}
			System.out.println();
		}
		
		
	}

}


